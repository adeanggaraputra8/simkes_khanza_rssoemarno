    private void BtnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSimpanActionPerformed
        if(tabMode.getRowCount()==0){
            JOptionPane.showMessageDialog(null, "Maaf, data pada tabel masih kosong ...!");
        } else {
                try {
                    for(int i=0; i<tbKamar.getRowCount(); i++){
                        String noRujukan = tbKamar.getValueAt(i,1).toString();
                        String kdDiagnosa = tbKamar.getValueAt(i,4).toString();
                        String tglAwal = tbKamar.getValueAt(i,5).toString();
                        String tglAkhir = tbKamar.getValueAt(i,6).toString();
                        String nmDiagnosa = Sequel.cariIsi("SELECT nm_penyakit FROM penyakit WHERE kd_penyakit='"+kdDiagnosa+"'");

                        // === CEK APAKAH DATA SUDAH ADA DI bridging_rujukan_bpjs_khusus ===
                        if(Sequel.cariInteger("SELECT COUNT(*) FROM bridging_rujukan_bpjs_khusus WHERE no_rujukan='"+noRujukan+"'") == 0){
                            // Belum ada â†’ INSERT
                            Sequel.menyimpan(
                                "bridging_rujukan_bpjs_khusus",
                                "?,?,?,?,?",
                                5,
                                new String[]{
                                    noRujukan,
                                    tbKamar.getValueAt(i,2).toString(),
                                    tbKamar.getValueAt(i,3).toString(),
                                    tglAwal,
                                    tglAkhir
                                }
                            );
                        } else {
                            // Sudah ada â†’ UPDATE
                            Sequel.mengedit(
                                "bridging_rujukan_bpjs_khusus",
                                "no_rujukan=?",
                                "tglrujukan_awal='"+tglAwal+"', tglrujukan_berakhir='"+tglAkhir+"'",
                                1,
                                new String[]{ noRujukan }
                            );
                        }

                        // === UNTUK TABEL DIAGNOSA ===
                        if(Sequel.cariInteger("SELECT COUNT(*) FROM bridging_rujukan_bpjs_khusus_diagnosa WHERE no_rujukan='"+noRujukan+"'") == 0){
                            Sequel.menyimpan(
                                "bridging_rujukan_bpjs_khusus_diagnosa",
                                "?,?,?,?",
                                4,
                                new String[]{
                                    noRujukan,
                                    "P",
                                    kdDiagnosa,
                                    nmDiagnosa
                                }
                            );
                        } else {
                            Sequel.mengedit(
                                "bridging_rujukan_bpjs_khusus_diagnosa",
                                "no_rujukan=?",
                                "kode_diagnosa='"+kdDiagnosa+"', nama_diagnosa='"+nmDiagnosa+"'",
                                1,
                                new String[]{ noRujukan }
                            );
                        }
                    }
                    JOptionPane.showMessageDialog(null, "Data berhasil disimpan/diupdate!");
                } catch (Exception ex) {
                    System.out.println("Notifikasi error: "+ex);
                    JOptionPane.showMessageDialog(null, "Terjadi kesalahan saat menyimpan data!");
                }
        }
    }//GEN-LAST:event_BtnSimpanActionPerformed

