    private void tampil() {
        Valid.tabelKosong(tabMode);
        try{
            ps=koneksi.prepareStatement( "select s.no_surat,s.no_rawat,rp.no_rkm_medis,p.nm_pasien," +
                    " s.tanggalawal,s.tanggalakhir,s.lamasakit,s.assesmen,s.keterangan,s.jenis" +
                    " from m_surat_kunh s inner join reg_periksa rp on s.no_rawat=rp.no_rawat" +
                    " inner join pasien p on rp.no_rkm_medis=p.no_rkm_medis "+
                    " WHERE s.tanggalawal between ? and ? and s.no_surat like '%"+TCari.getText().trim()+"%' or "+
                    " s.tanggalawal between ? and ? and s.no_rawat like '%"+TCari.getText().trim()+"%' or "+
                    " s.tanggalawal between ? and ? and rp.no_rkm_medis like '%"+TCari.getText().trim()+"%' or "+
                    " s.tanggalawal between ? and ? and p.nm_pasien like '%"+TCari.getText().trim()+"%' or "+
                    " s.tanggalawal between ? and ? and s.jenis like '%"+TCari.getText().trim()+"%' "+
                     "order by s.no_surat");
            try {
                ps.setString(1,Valid.SetTgl(DTPCari1.getSelectedItem()+""));
                ps.setString(2,Valid.SetTgl(DTPCari2.getSelectedItem()+""));
                ps.setString(3,Valid.SetTgl(DTPCari1.getSelectedItem()+""));
                ps.setString(4,Valid.SetTgl(DTPCari2.getSelectedItem()+""));
                ps.setString(5,Valid.SetTgl(DTPCari1.getSelectedItem()+""));
                ps.setString(6,Valid.SetTgl(DTPCari2.getSelectedItem()+""));
                ps.setString(7,Valid.SetTgl(DTPCari1.getSelectedItem()+""));
                ps.setString(8,Valid.SetTgl(DTPCari2.getSelectedItem()+""));
                ps.setString(9,Valid.SetTgl(DTPCari1.getSelectedItem()+""));
                ps.setString(10,Valid.SetTgl(DTPCari2.getSelectedItem()+""));
                rs=ps.executeQuery();
                while(rs.next()){
                    tabMode.addRow(new Object[]{rs.getString(1),
                                   rs.getString(2),
                                   rs.getString(3),
                                   rs.getString(4),
                                   rs.getString(5),
                                   rs.getString(6),
                                   rs.getString(7),
                                   rs.getString(8),
                                   rs.getString(9),
                                   rs.getString(10)
                    });
                }
            } catch (Exception e) {
                System.out.println(e);
            } finally{
                if(rs!=null){
                    rs.close();
                }
                if(ps!=null){
                    ps.close();
                }
            }
        }catch(Exception e){
            System.out.println("Notifikasi : "+e);
        }
        LCount.setText(""+tabMode.getRowCount());
    }

