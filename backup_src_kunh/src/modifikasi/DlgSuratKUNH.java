    private void getData() {
        if(tbDokter.getSelectedRow()!= -1){
            NoSurat.setText(tbDokter.getValueAt(tbDokter.getSelectedRow(),0).toString());
            TNoRw.setText(tbDokter.getValueAt(tbDokter.getSelectedRow(),1).toString());
            TNoRm.setText(tbDokter.getValueAt(tbDokter.getSelectedRow(),2).toString());
            TPasien.setText(tabMode.getValueAt(tbDokter.getSelectedRow(),3).toString());
            Valid.SetTgl(TanggalAwal,tbDokter.getValueAt(tbDokter.getSelectedRow(),4).toString());
            Valid.SetTgl(TanggalAkhir,tbDokter.getValueAt(tbDokter.getSelectedRow(),5).toString());
            Valid.SetTgl(TanggalPerkiraan,tbDokter.getValueAt(tbDokter.getSelectedRow(),5).toString());
            LamaSakit.setText(tbDokter.getValueAt(tbDokter.getSelectedRow(),6).toString());                     
            if(tbDokter.getValueAt(tbDokter.getSelectedRow(),9).toString().equals("Surat Sakit")){
                Valid.SetTgl(TanggalAkhir,tbDokter.getValueAt(tbDokter.getSelectedRow(),5).toString());
                TanggalPerkiraan.setDate(new Date());
                AssementCuti.setText("");
                KetCutiMelahirkan.setText("");
                Assement.setText("");
                KetSuratTerbang.setText("");
            }else if (tbDokter.getValueAt(tbDokter.getSelectedRow(),9).toString().equals("Surat Cuti Melahirkan")){
                Valid.SetTgl(TanggalPerkiraan,tbDokter.getValueAt(tbDokter.getSelectedRow(),5).toString());
                TanggalAkhir.setDate(new Date());
                KetCutiMelahirkan.setText(tabMode.getValueAt(tbDokter.getSelectedRow(),8).toString());
                 AssementCuti.setText(tbDokter.getValueAt(tbDokter.getSelectedRow(),7).toString()); 
                 Assement.setText("");
                KetSuratTerbang.setText("");        
            }else if (tbDokter.getValueAt(tbDokter.getSelectedRow(),9).toString().equals("Surat Terbang")){
                TanggalAwal.setDate(new Date());
                TanggalAkhir.setDate(new Date());
                TanggalPerkiraan.setDate(new Date());
                KetSuratTerbang.setText(tabMode.getValueAt(tbDokter.getSelectedRow(),8).toString());
                KetCutiMelahirkan.setText("");
                Assement.setText(tbDokter.getValueAt(tbDokter.getSelectedRow(),7).toString()); 
                AssementCuti.setText("");
            }
          
        }   
    }

